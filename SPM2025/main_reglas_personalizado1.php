        <h3>Reglas Generales</h3>
        <p>1. <strong>Equipos Únicos:</strong> Cada participante debe registrar un equipo con un nombre original y único. No se permitirán nombres repetidos.</p>
        <p><em>Ejemplo:</em> Si un participante elige el nombre "RacingStar", otro participante no podrá registrar el equipo con ese mismo nombre.</p>

        <p>2. <strong>Composición del Equipo:</strong> Cada equipo estará formado por 7 pilotos de MotoGP y 7 pilotos de Fórmula 1. Cada piloto tendrá un coste asignado en puntos, y tendrás un límite de 1400 puntos para armar tu equipo de cada competición.</p>
        <p><em>Ejemplo:</em> Si eliges a un piloto con un valor de 300 puntos y otro de 200 puntos, aún te quedarán 900 puntos para completar tu equipo de MotoGP o Fórmula 1.</p>

        <p>3. <strong>Resultados Oficiales:</strong> Las clasificaciones se basarán en los resultados oficiales de las carreras de MotoGP y Fórmula 1.</p>
        <p><em>Ejemplo:</em> Si tu piloto elegido queda en 1º lugar en una carrera de MotoGP, ganarás puntos según las reglas de puntuación que se explican más abajo.</p>

        <p>4. <strong>Cambios de Pilotos:</strong> En caso de que un piloto sea reemplazado durante la temporada, el piloto sustituto automáticamente reemplazará al piloto original en tu equipo.</p>
        <p><em>Ejemplo:</em> Si un piloto se lesiona y es reemplazado por otro en la escudería, este cambio también se reflejará en tu equipo.</p>

        <p>5. <strong>Actualización de Resultados:</strong> Los resultados de cada carrera se publicarán al finalizar la carrera, y las clasificaciones se actualizarán mensualmente.</p>
        <p><em>Ejemplo:</em> Si al finalizar el mes tu equipo tiene la mejor puntuación, aparecerás en el primer puesto de la clasificación mensual.</p>
